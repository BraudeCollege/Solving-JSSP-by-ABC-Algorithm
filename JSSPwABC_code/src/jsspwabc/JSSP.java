    JSSP(int sol[],int noj,int nom) {
        int d = nom*noj;
        int i,j;
        
        //System.out.println(noj + " " + nom + " " + d);
       
        startT = new int[nom][noj];
        endT = new int[nom][noj];
        
        int jwp[] = new int[noj];       //job working procedure
        int jom[] = new int[nom];       // job on machine
        int mjn[][] = new int[nom][noj];
        
        //calculatin start time and end time of each job on each machine
        for( i=0; i<d; i++ ) {
            if( jwp[sol[i]]==0 ) {
                if( jom[ jmS[sol[i]][0] ]==0 ) {
                    startT[jmS[sol[i]][0]][0] = 0;
                    endT[jmS[sol[i]][0]][0] = jpT[sol[i]][0];
                }
                else {
                    startT[jmS[sol[i]][0]][jom[jmS[sol[i]][0]]] = endT[jmS[sol[i]][0]][jom[jmS[sol[i]][0]]-1];
                    endT[jmS[sol[i]][0]][jom[jmS[sol[i]][0]]] = startT[jmS[sol[i]][0]][jom[jmS[sol[i]][0]]] + jpT[sol[i]][0];
                }
            }
            else {
                if( jom[jmS[sol[i]][jwp[sol[i]]]]==0 ) {
                    startT[jmS[sol[i]][jwp[sol[i]]]][0] = endT[jmS[sol[i]][jwp[sol[i]]-1]][mjn[jmS[sol[i]][jwp[sol[i]]-1]][sol[i]]];
                    endT[jmS[sol[i]][jwp[sol[i]]]][0] = startT[jmS[sol[i]][jwp[sol[i]]]][0] + jpT[sol[i]][jwp[sol[i]]];
                }
                else {
                    startT[jmS[sol[i]][jwp[sol[i]]]][jom[jmS[sol[i]][jwp[sol[i]]]]] = Math.max( endT[jmS[sol[i]][jwp[sol[i]]]][jom[jmS[sol[i]][jwp[sol[i]]]]-1], endT[jmS[sol[i]][jwp[sol[i]]-1]][mjn[jmS[sol[i]][jwp[sol[i]]-1]][sol[i]]]);
                    endT[jmS[sol[i]][jwp[sol[i]]]][jom[jmS[sol[i]][jwp[sol[i]]]]]   = startT[jmS[sol[i]][jwp[sol[i]]]][jom[jmS[sol[i]][jwp[sol[i]]]]] + jpT[sol[i]][jwp[sol[i]]];
                }
                
            }
            
            mjn[jmS[sol[i]][jwp[sol[i]]]][sol[i]] = jom[jmS[sol[i]][jwp[sol[i]]]];
            jom[jmS[sol[i]][jwp[sol[i]]]] += 1;
            jwp[sol[i]] += 1;

                        
        }
        
        //Calculating minimum makespan time
        for( i=0; i<nom; i++ ) 
            for( j=0; j<noj; j++ ) 
                minMakespanT = Math.max(endT[i][j], minMakespanT);
        
    }
    
      
